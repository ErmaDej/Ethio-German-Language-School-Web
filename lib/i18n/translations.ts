export function getTranslation(key: string, lang: Language = "en"): string {
  const keys = key.split(".")
  let value: any = translations[lang]

  for (const k of keys) {
    value = value?.[k]
  }

  return value || translations.en[key as keyof (typeof translations)["en"]] || key
}
